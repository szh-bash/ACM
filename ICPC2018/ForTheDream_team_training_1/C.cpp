int n, m, d, l;
double sum;
double f;
int read(){
    int p=0, q=1;
    char ch=getchar();
    while (!num(ch))
        (ch=='-'?q=-1:0),
        ch=getchar();
    while (num(ch)) p=p*10+ch-'0', ch=getchar();
    return p*q;
}
int power(ll a, int b){
	ll c=1;
	while (b){
		if (b&1) c=c*a%mo;
		a=a*a%mo;
		b>>=1;
	}
	return c;
}
int main(){
	for (int _=read();_;_--){
		cin>>d>>l;
		n=l*100000;
		m=d*100000;
		sum=f=0;
		for (int i=m+1;m<=n;i++)
			f=(double)sum/i+1,
			sum+=f;
		printf("%.6lf\n", f);
	}
    return 0;
}
