#define mo 1000000007
#define num(x) (x>='0' && x<='9')
typedef unsigned long long ull;
typedef long long ll;
using namespace std::tr1;
using namespace std;
ll readll(){
    ll p=0, q=1;
    char ch=getchar();
    while (!num(ch))
        (ch=='-'?q=-1:0),
        ch=getchar();
    while (num(ch)) p=p*10+ch-'0', ch=getchar();
    return p*q;
}
int power(ll a, int b){
	ll c=1;
	while (b){
		if (b&1) c=c*a%mo;
		a=a*a%mo;
		b>>=1;
	}
	return c;
}
int main(){
	ll x=readll(), k=readll(), d=readll();
	ll p=-x/d;
	ll ans;
	if (abs(p)>=k) ans=abs(x+k*d*(x<0?1:-1));
	else{
		k-=abs(p);
		if (x<0) p+=k&1;
		else p-=k&1;
		ans=abs(x+d*p);
	}
	cout<<ans<<endl;
    return 0;
}
