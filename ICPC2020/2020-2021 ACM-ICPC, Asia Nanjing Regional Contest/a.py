def graphRun(g, a, f):
    n, m = len(g), len(g[0])
    b = []
    for (x, y) in a:
        tx, ty = f(x, y)
        if 0 <= tx and tx < n and 0 <= ty and ty < m and g[tx][ty] != 0:
            b.append((tx, ty))
        else:
            b.append((x, y))
    # print(b)
    return b


