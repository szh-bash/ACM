typedef long long ll;
using namespace std;
int read(){
	int p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') ch=='-'?q=-1:0, ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
ll readll(){
	ll p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') ch=='-'?q=-1:0, ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
void solve(){
	ll n=read(), r=read();
	ll m=min(n,r);
	ll ans=0;
	if (m==n) ans++, m--;
	ans+=1ll*(1+m)*m/2;
	printf("%I64d\n", ans);
	
}
int main(){
	for (int _=read();_;_--) solve();
	return 0;
}
