typedef long long ll;
using namespace std;
int read(){
	int p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') ch=='-'?q=-1:0, ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
ll readll(){
	ll p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') ch=='-'?q=-1:0, ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
ll gcd(ll a, ll b){
	return b?gcd(b,a%b):a;
}
int main(){
	for (int _=read();_;_--){
		ll x=readll(), y=readll();
		if (x-y>1) cout<<"YES"<<endl;
		else cout<<"No"<<endl;
	}
	return 0;
}
