typedef long long ll;
using namespace std;
int n;
char s[1000000];
int read(){
	int p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') 
		ch=='-'?q=-1:0,
		ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
int main(){
	n=read();
	scanf("%s", s+1);
	ll ans=0;
	for (int i=1;i<=n;i++){
		if ((s[i]-'0')&1) continue;
		ans+=i;
	}
	cout<<ans<<endl;
	return 0;
}
