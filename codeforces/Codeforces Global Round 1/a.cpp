int read(){
	int p=0, q=1;
	char ch=getchar();
	while (ch<'0' || ch>'9') 
		ch=='-'?q=-1:0,
		ch=getchar();
	while (ch>='0' && ch<='9') p=p*10+ch-'0', ch=getchar();
	return p*q;
}
int main(){
	int b=read(), k=read();
	int ans=0;
	for (int i=1;i<=k;i++){
		ans=(ans*b+read())&1;
	}
	if (ans) cout<<"odd"<<endl;else cout<<"even"<<endl;
	return 0;
}
